##Calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas y el valor por hora

HrsTrabajadas=int(input("Ingrese la cantidad de horas trabajadas "))
ValorHora=int(input("Ingrese el valor por hora trabajada "))
sueldo=HrsTrabajadas*ValorHora
print("El sueldo es ")
print(sueldo)